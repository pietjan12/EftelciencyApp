package com.example.nickburgt.routeapp;

public class Coords {
    private double x = 0;
    private double y = 0;

    public Coords(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
